#include "../pipex.h"

int	main(int argc, char **argv, char **envp)
{
	pid_t	pid;

	exit_if_invalid_arg(argc);
	pid = fork();
	if (pid < 0)
		exit_fork_error();
	else if (pid > 0)
		wait(0);
	else
		run_cmd(pid, argv, envp);
	return (0);
}
